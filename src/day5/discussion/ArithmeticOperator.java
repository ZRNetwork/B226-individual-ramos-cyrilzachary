
package day5.discussion;

public class ArithmeticOperator {
    public static void main(String[] args) {
        int num = 50;
        int num2 = 10;
        int sum = num + num2;
        
        // add
        System.out.println(num + num2); // Concantenation if String + String
        // subtract
        System.out.println(num - num2);
        // multiply
        System.out.println(num * num2);
        // divide
        System.out.println(num / num2);
        // sum
        System.out.println(sum);
        
    }
}
